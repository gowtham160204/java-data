package class4;

public class Operator {

	public static void main (String[]args) {
		int a = 50;
		int b = 30;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		int c = 50;
		c++;
		System.out.println(c);
		int d = 20;
		d--;
		System.out.println(d);
	}
}
